/* Non-primitive data types: it also known as refrence data type, they are derived from primitive data types */

//That are Array, Objects, Functions

// 1. Array
const Avengers = ["IronMan", "CP America ", "Hulk", "SpiderMan"];
console.log(typeof Avengers);
console.log(Avengers);

// 2. Object
let myObj = {
    fname: "Sadab",
    lname: "Azhar",
    age: 20,
}
console.log(typeof myObj);
console.log(myObj);

// 3. Function
const myFunction = function(){
    console.log("Hii, I'm Sadab");
}
console.log(typeof myFunction);
console.log(myFunction);